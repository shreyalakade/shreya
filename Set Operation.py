E = {0, 2, 4, 6, 8}; # Frist set 
N = {1, 2, 3, 4, 5}; #second set
# E and N are two Sets
print("Union of E and N is",E | N) #Union of Sets

print("Intersection of E and N is",E & N) # Intersection of Sets

print("Difference of E and N is",E - N) # Difference of Sets

print("Symmetric difference of E and N is",E ^ N) # Symmetric Difference of Sets
